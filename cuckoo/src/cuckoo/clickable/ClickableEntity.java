package cuckoo.clickable;

public class ClickableEntity {
	private int num;
	private String userid;
	private String goodclick;
	private String badclick;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getGoodclick() {
		return goodclick;
	}
	public void setGoodclick(String goodclick) {
		this.goodclick = goodclick;
	}
	public String getBadclick() {
		return badclick;
	}
	public void setBadclick(String badclick) {
		this.badclick = badclick;
	}
	
}
